package com.ui.model;

public class Chapter {

	private int fellowship_id;
	private int chapter_id;
	private String fellowship_name;
	private String chapter_username;
	private String chapter_password;
	private String chapter_email;
	private String status;
	private int createdBy;
	private String createdDate;
	private String ipAddress;
	
	
	
	
	
	
	
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public int getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(int createdBy) {
		this.createdBy = createdBy;
	}
	public String getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	public String getIpAddress() {
		return ipAddress;
	}
	public void setIpAddress(String ipAddress) {
		this.ipAddress = ipAddress;
	}
	public int getFellowship_id() {
		return fellowship_id;
	}
	public void setFellowship_id(int fellowship_id) {
		this.fellowship_id = fellowship_id;
	}
	public int getChapter_id() {
		return chapter_id;
	}
	public void setChapter_id(int chapter_id) {
		this.chapter_id = chapter_id;
	}
	public String getFellowship_name() {
		return fellowship_name;
	}
	public void setFellowship_name(String fellowship_name) {
		this.fellowship_name = fellowship_name;
	}
	public String getChapter_username() {
		return chapter_username;
	}
	public void setChapter_username(String chapter_username) {
		this.chapter_username = chapter_username;
	}
	public String getChapter_password() {
		return chapter_password;
	}
	public void setChapter_password(String chapter_password) {
		this.chapter_password = chapter_password;
	}
	public String getChapter_email() {
		return chapter_email;
	}
	public void setChapter_email(String chapter_email) {
		this.chapter_email = chapter_email;
	}
	
	
	
	
	
	
}
